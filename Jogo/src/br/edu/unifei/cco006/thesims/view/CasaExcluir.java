    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        // TODO add your handling code here:
         
        casa = cdao.findByKey(Integer.parseInt(
                cbxCasa.getSelectedItem().toString()));
        cbxCasa.setEnabled(false);
        btnBuscar.setEnabled(false);
        btnExcluir.setEnabled(true);        
    }//GEN-LAST:event_btnBuscarActionPerformed

    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        // TODO add your handling code here:
        cidade.getConstrucoes().remove(casa);
        cidadeDao.update(cidade);
        cdao.delete(casa);
        cbxCasa.setEnabled(true);
        btnBuscar.setEnabled(true);
        btnExcluir.setEnabled(false);
        JOptionPane.showMessageDialog(this, "Casa excluida. ");
    }//GEN-LAST:event_btnExcluirActionPerformed

    private void btnBuscarCidadeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarCidadeActionPerformed
        // TODO add your handling code here:
        cidade = cidadeDao.findByKey(cbxCidade.getSelectedItem().toString());
        //Fazer o tratamento pra pegar s√≥ casas
        cbxCasa.removeAllItems();
        List<Construcao> lConstrucao = cidade.getConstrucoes();
        for (Construcao c : lConstrucao) {
            if(c instanceof Casa)
                cbxCasa.addItem(""+c.getCodigo());
        }
    }//GEN-LAST:event_btnBuscarCidadeActionPerformed


