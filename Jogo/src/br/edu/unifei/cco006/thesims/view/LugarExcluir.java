    private void btnBuscarCidadeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarCidadeActionPerformed
        // TODO add your handling code here:
        cidade = cdao.findByKey(cbxCidade.getSelectedItem().toString());
        //Fazer o tratamento pra pegar s√≥ lugar
        cbxLugar.removeAllItems();
        List<Construcao> lConstrucao = cidade.getConstrucoes();
        for (Construcao c : lConstrucao) {
            if(c instanceof Lugar)
                cbxLugar.addItem(""+c.getCodigo());
        }
        
    }//GEN-LAST:event_btnBuscarCidadeActionPerformed

    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        // TODO add your handling code here:
        List<Evento> lEvento = edao.findAll();
        for (Evento e : lEvento) {
            if(e.getLocal() == lugar){
                e.setLocal(null);
                edao.update(e);
            }
        }

        cidade.getConstrucoes().remove(lugar);
        

        ldao.delete(lugar);
        cdao.update(cidade);

        JOptionPane.showMessageDialog(this, "Lugar Excluido.");

    }//GEN-LAST:event_btnExcluirActionPerformed

